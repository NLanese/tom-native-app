const numberToMonth = (num) => {
    if (num == "01"){
        return "JAN"
    }
    if (num == "02"){
        return "FEB"
    }
    if (num == "03"){
        return "MAR"
    }
    if (num == "04"){
        return "APR"
    }
    if (num == "05"){
        return "MAY"
    }
    if (num == "06"){
        return "JUN"
    }
    if (num == "07"){
        return "JUL"
    }
    if (num == "08"){
        return "AUG"
    }
    if (num == "09"){
        return "SEP"
    }
    if (num == 10){
        "OCT"
    }
    if (num == 11){
        "NOV"
    }
    if (num == 12){
        "DEC"
    }
}

export default numberToMonth